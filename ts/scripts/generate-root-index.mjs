#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(fileURLToPath(import.meta.url), "..", "..");
const allowlistPath = join(root, "root-export-allowlist.json");
const indexPath = join(root, "src", "index.ts");

function readAllowlist() {
  const data = JSON.parse(readFileSync(allowlistPath, "utf8"));
  if (!Array.isArray(data.modules)) throw new Error("root-export-allowlist.json needs modules[]");
  return {
    modules: data.modules,
    resolutions: Array.isArray(data.resolutions) ? data.resolutions : [],
    aliases: Array.isArray(data.aliases) ? data.aliases : [],
  };
}

function renderIndex() {
  const { modules, resolutions, aliases } = readAllowlist();
  const lines = [
    "/**",
    " * autoctx — autocontext TypeScript toolkit.",
    " * Generated by scripts/generate-root-index.mjs from root-export-allowlist.json.",
    " * Do not hand-edit exports here; edit the allowlist or publish a subpath instead.",
    " */",
    "",
  ];
  for (const mod of modules) lines.push(`export * from ${JSON.stringify(mod)};`);
  if (resolutions.length) {
    lines.push("", "// Explicit resolutions for names exported by multiple allowed modules.");
    for (const item of resolutions) {
      const keyword = item.kind === "type" ? "export type" : "export";
      lines.push(`${keyword} { ${item.name} } from ${JSON.stringify(item.from)};`);
    }
  }
  if (aliases.length) {
    lines.push("", "// Backward-compatible root type aliases.");
    for (const alias of aliases) {
      lines.push(
        `export type { ${alias.name} as ${alias.as} } from ${JSON.stringify(alias.from)};`,
      );
    }
  }
  return `${lines.join("\n")}\n`;
}

const rendered = renderIndex();
if (process.argv.includes("--check")) {
  const current = readFileSync(indexPath, "utf8");
  if (current !== rendered) {
    console.error("ts/src/index.ts is out of sync; run `node scripts/generate-root-index.mjs`.");
    process.exit(1);
  }
} else {
  writeFileSync(indexPath, rendered);
}
